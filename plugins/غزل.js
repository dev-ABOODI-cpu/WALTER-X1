module.exports = {
    command: 'غزل',
    async execute(sock, m) {
        const chatId = m.key.remoteJid;

        if (!chatId.endsWith('@g.us')) {
            return sock.sendMessage(chatId, { text: `🚫 هذا الأمر يعمل فقط في المجموعات!` });
        }

        const mentionedJids = m.message?.extendedTextMessage?.contextInfo?.mentionedJid || [];

        if (!mentionedJids.length) {
            return sock.sendMessage(chatId, {
                text: `❌ استخدم الأمر مع منشن.\nمثال: .غزل @الشخص`
            });
        }

        const target = mentionedJids[0];

        const quotes = [
            // ✨ لطيف
            "وجودك يضيف للحياة طمأنينة جميلة ما تنوصف.",
            "ابتسامتك كأنها بداية يوم جميل جديد.",
            "فيك هدوء يخلي كل شيء حولك أهدأ.",
            "كأنك نسخة من الراحة النفسية.",
            "كل ما تمر، تترك أثر جميل بدون ما تحس.",

            // 🌸 مدح
            "فيك جاذبية بسيطة تخلي الناس تحب وجودك.",
            "أسلوبك في الكلام يخلّي أي حديث أجمل.",
            "أنت من الأشخاص اللي وجودهم يفرق فعلاً.",
            "فيك احترام وهدوء يلفت الانتباه.",
            "شخصيتك جميلة بدون أي مجهود.",

            // 💫 إحساس
            "أحياناً وجود شخص مثلك يكفي لتعديل المزاج كله.",
            "كأنك تفهم الحياة بطريقة مختلفة وهادئة.",
            "فيك شيء يخلي الناس ترتاح لك من أول لحظة.",
            "أنت من النوع اللي ما يتكرر بسهولة.",
            "حتى صمتك له معنى جميل.",

            // 🌷 خفيف غزل
            "ابتسامتك تكفي تغير يوم كامل.",
            "فيك جمال بسيط لكنه ملفت بطريقة لطيفة.",
            "كل ما شفتك، يزيد الإعجاب بدون سبب واضح.",
            "وجودك لحاله كفاية يكون جميل.",
            "كأنك تفهم معنى اللطف بدون ما تتكلم.",

            // 🌙 عميق
            "بعض الأشخاص وجودهم يشرح معنى الجمال الحقيقي.",
            "أنت مثال للبساطة اللي تخطف الانتباه.",
            "فيك هدوء نادر وسط ضجيج العالم.",
            "كل شيء فيك يوحي بالراحة والقبول.",
            "حتى حضورك من بعيد يبان مختلف.",

            // 💛 إضافي
            "فيك طاقة إيجابية تخلي المكان أجمل.",
            "لو للجمال معنى، ممكن يكون قريب منك.",
            "أسلوبك يخلي أي لحظة ألطف.",
            "كأنك تفصيل صغير لكنه مهم في يوم الناس.",
            "وجودك لحاله كافي يخلي المزاج أفضل."
        ];

        const randomQuote = quotes[Math.floor(Math.random() * quotes.length)];

        const text = `💬 @${target.split('@')[0]}، ${randomQuote}`;

        await sock.sendMessage(chatId, {
            text,
            mentions: [target]
        });
    }
};